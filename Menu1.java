    private void btnResetAddbillActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnResetAddbillActionPerformed
        txtCode.setText("");
        txtOldindex.setText("");
        txtNewindex.setText("");
        txtCode.requestFocus();
        j = 1;
        model.setRowCount(0);
        for (int i = 0; i < brr.size(); i++) {
            showTable(brr.get(i));
        }

    }//GEN-LAST:event_btnResetAddbillActionPerformed
    public int count(ArrayList<Bill> brr) {
        int dem = 0;
        for (int i = 0; i < brr.size(); i++) {
            if (txtCode.getText().equals(brr.get(i).getCode())) {
                dem++;
            }
        }
        return dem;

    }
    private void btnAddbillActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddbillActionPerformed
        // TODO add your handling code here:
        Bill f = new Bill();
        if (brr.size() == 0) {
            try {
                f.setCode(txtCode.getText());
                f.setType(cbxType.getSelectedItem().toString());
                f.setOldindex(Double.parseDouble(txtOldindex.getText()));
                f.setNewindex(Double.parseDouble(txtNewindex.getText()));
                brr.add(f);
                showTable(f);
                BillList b = new BillList();
                b.SaveBill(brr);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Dữ liệu nhập vào không đúng!!", "Thông báo lỗi", JOptionPane.ERROR_MESSAGE);
                txtCode.setText("");
                txtOldindex.setText("");
                txtNewindex.setText("");
                txtCode.requestFocus();
            }
        } else if (brr.size() > 0) {
            if (count(brr) != 0) {
                JOptionPane.showMessageDialog(this, "Mã hóa đơn này đã được sử dụng!!", "Thông báo lỗi", JOptionPane.ERROR_MESSAGE);
                txtCode.setText("");
                txtOldindex.setText("");
                txtNewindex.setText("");
                txtCode.requestFocus();
            } else {
                try {
                    f.setCode(txtCode.getText());
                    f.setType(cbxType.getSelectedItem().toString());
                    f.setOldindex(Double.parseDouble(txtOldindex.getText()));
                    f.setNewindex(Double.parseDouble(txtNewindex.getText()));
                    brr.add(f);
                    showTable(f);
                    BillList b = new BillList();
                    b.SaveBill(brr);

                } catch (Exception e) {
                    JOptionPane.showMessageDialog(this, "Dữ liệu nhập vào không đúng!!", "Thông báo lỗi", JOptionPane.ERROR_MESSAGE);
                    txtCode.setText("");
                    txtOldindex.setText("");
                    txtNewindex.setText("");
                    txtCode.requestFocus();
                }
            }
        }

    }
    int j = 1;

    public void showTable(Bill f) {
        model.addRow(new Object[]{
            j++, f.getCode(), f.getType(), f.getOldindex(), f.getNewindex(), f.Payment()
        });

    }//GEN-LAST:event_btnAddbillActionPerformed


