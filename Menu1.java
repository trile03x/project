    private void btnsearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsearchActionPerformed
        // TODO add your handling code here:
        boolean dem = false;
        for (int i = 0; i < brr.size(); i++) {
            if (txtcodefind.getText().equals(brr.get(i).getCode())) {
                codeupdate = txtcodefind.getText();
                dem = true;
                String resultFindMax = "\t          HÓA ĐƠN " + brr.get(i).getCode()
                        + "\n=================================="
                        + "\n- Loại hóa đơn : " + brr.get(i).getType()
                        + "\n- Chỉ số cũ : " + brr.get(i).getOldindex()
                        + "\n- Chỉ số mới : " + brr.get(i).getNewindex()
                        + "\n- Định mức sử dụng điện (KW) : " + brr.get(i).Norm()
                        + "\n- Chỉ số điện đã sử dụng : " + brr.get(i).UsedElectricalEnergy()
                        + "\n- Số tiền thanh toán : " + brr.get(i).Payment() + " VNĐ";
                txtResultFindBill.setText(resultFindMax);
                j =1;
                model.setRowCount(0);
                showTable(brr.get(i));
            }
        }
        if (dem == false) {
            txtResultFindBill.setText("");
            txtcodefind.setText("");
            txtcodefind.requestFocus();
            JOptionPane.showMessageDialog(this, "Mã hóa đơn không tồn tại", "Lỗi", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnsearchActionPerformed

