    private void btnDeleteBillActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteBillActionPerformed
        // TODO add your handling code here:
        String str = txtCodeRemove.getText();
        int dem = 0;
        for (int i = 0; i < brr.size(); i++) {
            if (brr.get(i).getCode().equals(str)) {
                JOptionPane.showMessageDialog(this, "Xóa thành công");
                brr.remove(i);
                txtCodeRemove.setText("");
                dem++;
//                break;
            }
        }
        j = 1;
        model.setRowCount(0);
        for (int i = 0; i < brr.size(); i++) {
            showTable(brr.get(i));
        }
        BillList f = new BillList();
        f.SaveBill(brr);
        if (dem != 1) {
            JOptionPane.showMessageDialog(this, "Mã hóa đơn không tồn tại", "Lỗi", JOptionPane.ERROR_MESSAGE);
            txtCodeRemove.setText("");
            txtCodeRemove.requestFocus();
        }
    }//GEN-LAST:event_btnDeleteBillActionPerformed

