    private void btnFindMaxActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFindMaxActionPerformed
        // TODO add your handling code here:
        String MinMax = cbxMinMax.getSelectedItem().toString();
        if (MinMax.equals("LỚN NHẤT")) {
            BillList p = new BillList();
            ArrayList<Bill> arr = new ArrayList<>();
            arr = p.MaxPayment(brr);
            j = 1;
            model.setRowCount(0);
            for (int i = 0; i < arr.size(); i++) {
                showTable(arr.get(i));
            }
        } else if (MinMax.equals("NHỎ NHẤT")) {
            BillList p = new BillList();
            ArrayList<Bill> arr = new ArrayList<>();
            arr = p.MinPayment(brr);
            j = 1;
            model.setRowCount(0);
            for (int i = 0; i < arr.size(); i++) {
                showTable(arr.get(i));
            }
        }
    }//GEN-LAST:event_btnFindMaxActionPerformed

